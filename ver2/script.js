let lifePoints = 30.0; // 初期ライフポイントを小数点第1位まで設定
let isGameActive = false;
let timerInterval;
let correctCount = 0;
let mode = 'normal'; // デフォルトモードを設定
let recoveryRate = 3.0; // 初期回復量

const targetNameElement = document.getElementById('target-name');
const imageGridElement = document.getElementById('image-grid');
const lifePointsElement = document.getElementById('life-points');
const exitButton = document.getElementById('exit-button'); // ボタンの取得
const startButton = document.getElementById('start-button'); // スタートボタン
const statsElement = document.getElementById('stats'); // 統計情報を表示する要素

// ゲームを初期化
function initGame(selectedMode) {
    mode = selectedMode;
    if (mode === 'time-trial') {
        lifePoints = 90.0; // 90秒トライアルの初期ライフポイント
    } else if (mode === 'endless-challenge') {
        lifePoints = 30.0; // 無限チャレンジの初期ライフポイント
    } else {
        lifePoints = 90.0; // デフォルトは通常の90ライフポイント
    }
    correctCount = 0;
    recoveryRate = 3.0; // 初期回復量
    isGameActive = true;
    updateLifePoints();
    updateStats(); // 統計情報を更新
    startTimer();
    loadNextRound();
}

// ライフポイントを更新
function updateLifePoints() {
    lifePointsElement.textContent = lifePoints.toFixed(1); // 小数点第1位まで表示

    // 赤いゲージの割合（最大30まで）
    const lifePercentage = Math.min(100, (Math.min(lifePoints, 30) / 30) * 100);
    // 青いゲージの割合（30を超えた分、最大30まで）
    const extraLifePercentage = Math.max(0, Math.min(lifePoints - 30, 30) / 30 * 100);
    // オレンジゲージの割合（60を超えた分、最大30まで）
    const orangeLifePercentage = Math.max(0, Math.min(lifePoints - 60, 30) / 30 * 100);

    // 赤いゲージ（最大30まで）
    document.getElementById('life-bar-red').style.width = `${(lifePercentage / 100) * 300}px`;

    // 青いゲージ（30を超えた分）
    document.getElementById('life-bar-blue').style.width = `${(extraLifePercentage / 100) * 300}px`;

    // オレンジゲージ（60を超えた分）
    document.getElementById('life-bar-orange').style.width = `${(orangeLifePercentage / 100) * 300}px`;
}

// 統計情報を更新
function updateStats() {
    statsElement.textContent = `正解回数: ${correctCount}`;
}

// タイマーを開始
function startTimer() {
    clearInterval(timerInterval);
    timerInterval = setInterval(() => {
        if (lifePoints > 0 && isGameActive) {
            lifePoints -= 1.0;
            updateLifePoints();
        } else if (isGameActive) {
            endGame();
        }
    }, 1000);
}

// 次のラウンドを読み込む
function loadNextRound() {
    if (images.length === 0) {
        console.error('画像リストが空です');
        return;
    }

    imageGridElement.innerHTML = '';

    const correctImage = images[Math.floor(Math.random() * images.length)];
    targetNameElement.textContent = correctImage.name;

    const selectedImages = [correctImage]; // 正解画像を選択
    const correctIndex = Math.floor(Math.random() * 9);  // 9個の中から正解をランダムに選択

    for (let i = 0; i < 9; i++) {  // 9枚の画像を配置
        const img = document.createElement('img');

        let randomImage;
        do {
            randomImage = images[Math.floor(Math.random() * images.length)];
        } while (selectedImages.includes(randomImage)); // 同じ画像が選ばれないようにチェック

        if (i === correctIndex) {
            img.src = correctImage.url;
        } else {
            img.src = randomImage.url;
        }

        selectedImages.push(randomImage); // 画像を選択リストに追加
        img.addEventListener('click', () => handleImageClick(img, correctImage.url));
        imageGridElement.appendChild(img);
    }
}

// 画像クリック時の処理
function handleImageClick(img, correctSrc) {
    if (!isGameActive) return;

    if (img.src === correctSrc) {
        img.classList.add('correct');
        img.classList.remove('incorrect');
        correctCount++;
        if (mode === 'endless-challenge') {
            if (correctCount % 10 === 0) {
                recoveryRate *= 0.937; // 正解を10回ごとに回復量を93.7%に減少
            }
            lifePoints += recoveryRate; // 累積的に減少する回復量
            // ライフポイントの上限を90に制限
            lifePoints = Math.min(lifePoints, 90.0);
        }
        updateStats();
        updateLifePoints();
        setTimeout(() => loadNextRound(), 500);
    } else {
        img.classList.add('incorrect');
        img.classList.remove('correct');
        lifePoints -= 1.0; // 間違えるごとにライフポイント-1
        updateLifePoints();
        if (lifePoints <= 0) {
            endGame();
        }
    }
}

// ゲーム終了
function endGame() {
    isGameActive = false;
    clearInterval(timerInterval);
    document.querySelectorAll('#image-grid img').forEach(img => img.classList.add('game-over'));
    setTimeout(() => {
        window.location.href = `score.html?correct=${correctCount}`;
    }, 1000);
}

// 終了ボタンの処理
exitButton.addEventListener('click', () => {
    lifePoints = 0;
    updateLifePoints();
    endGame();
});

// スタートボタンの処理
startButton.addEventListener('click', () => {
    const urlParams = new URLSearchParams(window.location.search);
    const mode = urlParams.get('mode');
    initGame(mode);
    startButton.disabled = true;
});